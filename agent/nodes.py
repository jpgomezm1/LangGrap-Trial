    final_message = "Te he enviado la cotizaci√≥n a tu correo y tambi√©n adjunta aqu√≠ en el chat. Si tienes alguna otra pregunta, no dudes en consultarme."
    
    # La responsabilidad de enviar el mensaje y el documento por Telegram
