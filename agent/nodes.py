    quotation_pdf_path = state.get("quotation_pdf_path")
    
    if not quotation_pdf_path:
        print("Error: No se encontr√≥ la ruta del PDF de la cotizaci√≥n en el estado.")
        logger.error("No se encontr√≥ la ruta del PDF de la cotizaci√≥n en el estado.")
        state['response'] = "Hubo un problema generando el documento. Nuestro equipo comercial se pondr√° en contacto contigo."
